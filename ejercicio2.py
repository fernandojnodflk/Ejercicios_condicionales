"""
Programa que pida un número y diga si es positivo, negativo o 0.
"""
print("HOLA :)")
numero = int(input("Ingresa un número: "))
if numero > 0:
    print("El número es positivo.")
elif numero < 0:
    print("El número es negativo.")
else:
    print("El número es 0.")

